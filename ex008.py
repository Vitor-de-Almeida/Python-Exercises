#write a program that reads the value in meters and displays it converted to centimeters and millimeters
n1 = float(input('Enter the corresponding value in meters to obtain the said quantity in centimeters and millimeters:'))
cm = n1*100
mm = n1*1000
print(f'The corresponding value in centimeters is {cm:.1f} cm and in millimeters is {mm:.1f} mm!')

