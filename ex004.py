#make a program that reads something from the keyboard and shows on the screen its primitive type and all possible information about it!
n1 = input("Enter something:")
print(n1.isdecimal())
print(n1.isalnum())
print(n1.isalpha())
print(n1.isdigit())
print(n1.isprintable())
print(n1.istitle())
print(n1.isspace())
